#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t space)
{
	size_t	i;

	i = 0;
	if (space > 0)
	{
		while (src[i] && i < (space - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	while (src[i])
		i++;
	return (i);
}
